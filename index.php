<?php

/**
 * Скрипт для поиска и вывода имен файлов в папке /datafiles, удовлетворяющих определенным условиям.
 */

// Путь к папке с файлами
$folderPath = './src/datafiles';

// Получаем список файлов в папке
$files = scandir($folderPath);

// Регулярное выражение для проверки имени файла
$pattern = '/^[a-zA-Z0-9]+\.[iI][xX][tT]$/';

// Массив для хранения имен файлов, удовлетворяющих условиям
$matchingFiles = [];

// Перебираем файлы и проверяем их имена
foreach ($files as $file) {
    // Проверяем, что файл не является текущей директорией (".") или родительской директорией ("..")
    if ($file !== '.' && $file !== '..') {
        // Проверяем имя файла с помощью регулярного выражения
        if (preg_match($pattern, $file)) {
            // Добавляем имя файла в массив
            $matchingFiles[] = $file;
        }
    }
}

// Сортируем имена файлов по алфавиту
sort($matchingFiles);

// Выводим имена файлов на экран
foreach ($matchingFiles as $file) {
    echo $file . "\n";
}
